def get_calibration_stats() -> dict:
    """
    Compute EWMA-weighted calibration statistics from resolved legs.

    Groups legs by *market_type* (plus an ``"overall"`` aggregate) using the
    most recent ``_CALIBRATION_WINDOW`` resolved (W or L) legs.

    Returns
    -------
    dict mapping group_key → {
        ``n``          : int   — resolved leg count in window
        ``predicted``  : float — EWMA-weighted predicted probability (%)
        ``hit_rate``   : float — EWMA-weighted actual hit rate (%)
        ``calibration``: float — hit_rate / predicted  (1.0 = perfect)
        ``bias``       : str   — "OVERCONFIDENT" | "UNDERCONFIDENT" | "OK"
    }
    """
    _migrate_json_if_needed()
    # Use a subquery to LIMIT before ordering so the DB only scans the window.
    _sql = (
        "SELECT market_type, predicted_prob, result "
        "FROM (SELECT market_type, predicted_prob, result, id "
        "      FROM parlay_legs "
        "      WHERE result IN ('W','L') AND predicted_prob IS NOT NULL "
        "      ORDER BY id DESC LIMIT ?) "
        "ORDER BY id ASC"
    )
    with _db() as conn:
        rows = conn.execute(_sql, (_CALIBRATION_WINDOW,)).fetchall()

    if not rows:
        return {}

    # Build per-market groups: list of (predicted_prob, outcome) tuples
    groups: dict = {}
    for row in rows:
        key = row["market_type"] or "unknown"
        groups.setdefault(key, [])
        groups[key].append((float(row["predicted_prob"]), 1 if row["result"] == "W" else 0))

    stats: dict = {}
    for mtype, data in groups.items():
        stats[mtype] = _ewma_stats(data)

    # Overall across all market types
    all_data = [(float(r["predicted_prob"]), 1 if r["result"] == "W" else 0) for r in rows]
    stats["overall"] = _ewma_stats(all_data)

    return stats


