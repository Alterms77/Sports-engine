# ── Database helpers ───────────────────────────────────────────────────────────

def _ensure_db() -> None:
    """Create the database and tables if they do not yet exist."""
    os.makedirs(_DATA_DIR, exist_ok=True)
    with sqlite3.connect(_DB_FILE) as conn:
        conn.executescript(_DDL)


@contextmanager
def _db():
    """
    Context manager yielding a ``sqlite3.Connection`` with row-factory set.

    Automatically commits on clean exit, rolls back on exception, and ensures
    the schema exists before yielding.
    """
    _ensure_db()
    conn = sqlite3.connect(_DB_FILE, check_same_thread=False)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA foreign_keys = ON")
    try:
        yield conn
        conn.commit()
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()


# ── Legacy JSON migration ──────────────────────────────────────────────────────

def _migrate_json_if_needed() -> None:
    """
    One-time migration of ``parlay_history.json`` → SQLite.

    After a successful migration the JSON file is renamed to ``.bak`` so it
    is preserved for reference but no longer loaded.
    """
    if not os.path.exists(_LEGACY_JSON):
        return
    # Already migrated?
    bak = _LEGACY_JSON + ".bak"
    if os.path.exists(bak):
        return

    try:
        with open(_LEGACY_JSON, "r", encoding="utf-8") as fh:
            records = json.load(fh)
        if not isinstance(records, list):
            records = []
    except (json.JSONDecodeError, OSError) as exc:
        logger.warning("parlay_history: could not read legacy JSON for migration: %s", exc)
        return

    migrated = 0
    with _db() as conn:
        for rec in records:
            pid = rec.get("id", "")
            if not pid:
                continue
            # Skip if already in DB (idempotent)
            existing = conn.execute(
                "SELECT 1 FROM parlays WHERE id = ?", (pid,)
            ).fetchone()
            if existing:
                continue
            conn.execute(
                "INSERT INTO parlays(id, created_at, tier, combined_prob, parlay_result) "
                "VALUES (?,?,?,?,?)",
                (
                    pid,
                    rec.get("created_at", ""),
                    rec.get("tier", ""),
                    rec.get("combined_prob"),
                    rec.get("parlay_result"),
                ),
            )
            for order, leg in enumerate(rec.get("legs", [])):
                conn.execute(
                    "INSERT INTO parlay_legs"
                    "(parlay_id, leg_order, match_name, pick, market_type, "
                    " sport, league, sport_emoji, predicted_prob, result) "
                    "VALUES (?,?,?,?,?,?,?,?,?,?)",
                    (
                        pid, order,
                        leg.get("match", ""),
                        leg.get("pick", ""),
                        leg.get("market_type", ""),
                        leg.get("sport", ""),
                        leg.get("league", ""),
                        leg.get("sport_emoji", ""),
                        leg.get("predicted_prob"),
                        leg.get("result"),
                    ),
                )
            migrated += 1

    if migrated:
        logger.info("parlay_history: migrated %d records from JSON → SQLite", migrated)

    try:
        os.rename(_LEGACY_JSON, bak)
    except OSError as exc:
        logger.warning("parlay_history: could not rename legacy JSON: %s", exc)


# ── ID generation ──────────────────────────────────────────────────────────────

def _next_id(conn: sqlite3.Connection) -> str:
    """Return the next sequential parlay ID for today, e.g. ``P260315-3``."""
    today  = datetime.now(timezone.utc).strftime("%y%m%d")
    prefix = f"P{today}-"
    row    = conn.execute(
        "SELECT COUNT(*) FROM parlays WHERE id LIKE ?", (prefix + "%",)
    ).fetchone()
    n = row[0] if row else 0
    return f"{prefix}{n + 1}"


# ── Public API — write ─────────────────────────────────────────────────────────

def save_parlay(legs: list, tier: str, combined_prob: Optional[float]) -> str:
    """
    Persist a generated parlay and return its unique ID.

    Parameters
    ----------
    legs         : list of leg dicts from ``generate_parlay_legs``.
                   Expected keys: match, pick, market_type, sport_emoji,
                   prob (or predicted_prob), league, sport (all optional).
    tier         : "safe" | "balanced" | "risky"
    combined_prob: combined probability (%) for this tier's legs

    Returns
    -------
    Unique parlay ID string (e.g. ``"P260315-1"``).
    """
    _migrate_json_if_needed()
    with _LOCK:
        with _db() as conn:
            pid = _next_id(conn)
            now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
            conn.execute(
                "INSERT INTO parlays(id, created_at, tier, combined_prob, parlay_result) "
                "VALUES (?,?,?,?,?)",
                (pid, now, tier, combined_prob, None),
            )
            for order, leg in enumerate(legs):
                prob = leg.get("prob") if leg.get("prob") is not None else leg.get("predicted_prob")
                conn.execute(
                    "INSERT INTO parlay_legs"
                    "(parlay_id, leg_order, match_name, pick, market_type, "
                    " sport, league, sport_emoji, predicted_prob, result) "
                    "VALUES (?,?,?,?,?,?,?,?,?,?)",
                    (
                        pid, order,
                        leg.get("match", ""),
                        leg.get("pick", ""),
                        leg.get("market_type", ""),
                        leg.get("sport", ""),
                        leg.get("league", ""),
                        leg.get("sport_emoji", ""),
                        prob,
                        None,
                    ),
                )
        logger.info("parlay_history: saved %s (%d legs, tier=%s)", pid, len(legs), tier)
    return pid


def record_results(parlay_id: str, results: list) -> dict:
    """
    Record win/loss/void results for the legs of a previously saved parlay.

    Parameters
    ----------
    parlay_id : parlay ID string (e.g. ``"P240315-1"``) — case-insensitive.
    results   : list of ``"W"``, ``"L"``, or ``"X"`` (void/cancelled), one
                element per leg.  If shorter than the leg count, remaining
                legs are left as ``None``.

    Returns
    -------
    dict:
      ``found``   – bool
      ``id``      – parlay_id
      ``legs``    – updated legs list (only when found)
      ``overall`` – ``"W"`` / ``"L"`` / ``"X"`` / ``None``
    """
    _migrate_json_if_needed()
    pid_upper = parlay_id.strip().upper()

    with _LOCK:
        with _db() as conn:
            parlay_row = conn.execute(
                "SELECT id FROM parlays WHERE UPPER(id) = ?", (pid_upper,)
            ).fetchone()
            if not parlay_row:
                return {"found": False, "id": parlay_id}

            real_pid = parlay_row["id"]

            # Apply results to each leg by order
            for i, res in enumerate(results):
                conn.execute(
                    "UPDATE parlay_legs SET result = ? "
                    "WHERE parlay_id = ? AND leg_order = ?",
                    (res.strip().upper(), real_pid, i),
                )

            # Re-read all leg results
            leg_rows = conn.execute(
                "SELECT leg_order, match_name, pick, result "
                "FROM parlay_legs WHERE parlay_id = ? ORDER BY leg_order",
                (real_pid,),
            ).fetchall()

            all_results       = [r["result"] for r in leg_rows]
            reported_non_null = [r for r in all_results if r is not None]

            if any(r == "L" for r in reported_non_null):
                overall = "L"
            elif (
                len(reported_non_null) == len(leg_rows)
                and all(r in ("W", "X") for r in reported_non_null)
            ):
                overall = "W"
            else:
                overall = None

            conn.execute(
                "UPDATE parlays SET parlay_result = ? WHERE id = ?",
                (overall, real_pid),
            )

            logger.info(
                "parlay_history: recorded results for %s → %s", real_pid, overall
            )
            legs = [
                {
                    "match": r["match_name"],
                    "pick":  r["pick"],
                    "result": r["result"],
                }
                for r in leg_rows
            ]
            return {
                "found":   True,
                "id":      real_pid,
                "legs":    legs,
                "overall": overall,
            }


def get_last_parlay_id() -> Optional[str]:
    """Return the ID of the most recently generated parlay, or None."""
    _migrate_json_if_needed()
    with _db() as conn:
        row = conn.execute(
            "SELECT id FROM parlays ORDER BY created_at DESC, ROWID DESC LIMIT 1"
        ).fetchone()
    return row["id"] if row else None


