# ── Concurrency ────────────────────────────────────────────────────────────────
# SQLite handles internal locking; this module-level lock serialises the
# rarely-needed migration and heavy write operations from the bot's event loop.
_LOCK = threading.Lock()

# ── Calibration parameters ─────────────────────────────────────────────────────
# Minimum resolved legs in a group before its calibration factor is trusted.
_MIN_SAMPLES = 5
# Rolling window — only the most recent N resolved legs feed calibration.
# A finite window lets the model adapt to recent form rather than being
# anchored by old data that no longer reflects the current market.
# Override via PARLAY_CAL_WINDOW env var (e.g. "100" for a high-volume bot).
_CALIBRATION_WINDOW: int = int(os.environ.get("PARLAY_CAL_WINDOW", "200"))
# Calibration factor is clamped to this range to prevent extreme corrections
# from small or noisy samples.
_CAL_MIN, _CAL_MAX = 0.60, 1.40
# EWMA decay factor: 0 < EWMA_DECAY ≤ 1.0.
# Values < 1 give more weight to recent resolved legs.
# 1.0 = simple mean (no decay).  0.95 = mild recency bias.
# Override via PARLAY_EWMA_DECAY env var (e.g. "0.90" for faster adaptation).
EWMA_DECAY: float = float(os.environ.get("PARLAY_EWMA_DECAY", "0.95"))

# Compiled regex for validating / matching parlay IDs (e.g. "P240315-2").
# Exported so callers (bot.py) can reuse it without duplicating the pattern.
PARLAY_ID_RE = _re.compile(r'^P\d{6}-\d+$', _re.IGNORECASE)

