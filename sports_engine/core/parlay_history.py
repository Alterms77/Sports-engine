def calibrate_prob(prob: float, market_type: str) -> float:
    """
    Return a calibration-adjusted probability for a given market type.

    Falls back to the overall calibration factor when the per-market group
    has fewer than ``_MIN_SAMPLES`` resolved legs.  Returns ``prob`` unchanged
    when no calibration data is available yet.

    The correction factor is clamped to ``[_CAL_MIN, _CAL_MAX]`` to prevent
    extreme swings from small or noisy samples.
    """
    stats  = get_calibration_stats()
    factor = _get_factor(stats.get(market_type))
    if factor is None:
        factor = _get_factor(stats.get("overall"))
    if factor is None:
        return prob  # no data yet — return unchanged

    adjusted = prob * factor
    return round(max(1.0, min(99.0, adjusted)), 1)


# ── EWMA helpers ───────────────────────────────────────────────────────────────

def _ewma_stats(data: list) -> dict:
    """
    Compute EWMA-weighted calibration stats from a list of (predicted, outcome) tuples.

    The most recent entry (last in list) gets weight 1.0; earlier entries get
    ``EWMA_DECAY^k`` where k is how many steps back they are.
    """
    n = len(data)
    if n == 0:
        return {"n": 0, "predicted": 0.0, "hit_rate": 0.0,
                "calibration": 1.0, "bias": "OK"}

    weights = [EWMA_DECAY ** (n - 1 - i) for i in range(n)]
    w_sum   = sum(weights)

    w_pred = sum(w * d[0] for w, d in zip(weights, data)) / w_sum
    w_hit  = sum(w * d[1] for w, d in zip(weights, data)) / w_sum * 100.0

    cal = w_hit / w_pred if w_pred > 0 else 1.0
    return {
        "n":           n,
        "predicted":   round(w_pred, 1),
        "hit_rate":    round(w_hit, 1),
        "calibration": round(cal, 3),
        "bias":        _bias_label(cal),
    }


def _bias_label(cal: float) -> str:
    if cal < 0.85:
        return "OVERCONFIDENT"
    if cal > 1.15:
        return "UNDERCONFIDENT"
    return "OK"


def _get_factor(group_stats: Optional[dict]) -> Optional[float]:
    """Return clamped calibration factor from a stats dict, or None."""
    if not group_stats or group_stats["n"] < _MIN_SAMPLES:
        return None
    raw = group_stats["calibration"]
    return max(_CAL_MIN, min(_CAL_MAX, raw))


# ── Deep analytics ─────────────────────────────────────────────────────────────

def get_trend(n_last: int = 20) -> list:
    """
    Return the win/loss trend for the last ``n_last`` *resolved* parlays.

    Returns
    -------
    list of dicts: [{id, created_at, result, combined_prob}, …] oldest-first.
    """
    _migrate_json_if_needed()
    with _db() as conn:
        rows = conn.execute(
            "SELECT id, created_at, parlay_result, combined_prob "
            "FROM parlays WHERE parlay_result IN ('W','L') "
            "ORDER BY created_at DESC, ROWID DESC LIMIT ?",
            (n_last,),
        ).fetchall()
    return list(reversed([dict(r) for r in rows]))


