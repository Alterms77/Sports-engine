"""
Parlay History — SQLite-backed persistent storage with adaptive calibration.

Every parlay generated by ``/parlay`` is saved here.  When the user reports
results with ``/resultado``, each leg is marked W(on) / L(ost) / X(void).
That accumulated data feeds a calibration engine that automatically corrects
future probability estimates, making the parlay builder smarter over time.

Storage
-------
``data/parlay_history.db`` — SQLite database (no external dependencies).

Schema::

    parlays(id TEXT PK, created_at TEXT, tier TEXT,
            combined_prob REAL, parlay_result TEXT)

    parlay_legs(id INT PK AUTOINCREMENT, parlay_id TEXT FK,
                leg_order INT, match_name TEXT, pick TEXT,
                market_type TEXT, sport TEXT, league TEXT,
                sport_emoji TEXT, predicted_prob REAL, result TEXT)

If a legacy ``data/parlay_history.json`` file exists it is migrated into the
database on the first access and then renamed to ``.bak``.

Calibration logic
-----------------
For every group (market_type, and optionally sport/league) with ≥
``_MIN_SAMPLES`` resolved legs:

  EWMA-weighted calibration factor:

    w_i = DECAY ^ (N - 1 - i)    (i=0 oldest … i=N-1 most recent)
    predicted_bar = sum(w_i * pred_i) / sum(w_i)
    hit_rate_bar  = sum(w_i * outcome_i) / sum(w_i) * 100

    calibration_factor = hit_rate_bar / predicted_bar

  - Factor < 1 → model is *over*-confident → future probs are scaled down
  - Factor > 1 → model is *under*-confident → future probs are scaled up
  - Clamped to [``_CAL_MIN``, ``_CAL_MAX``] to avoid extreme swings

  Setting ``EWMA_DECAY = 1.0`` degrades gracefully to a simple mean.
"""

